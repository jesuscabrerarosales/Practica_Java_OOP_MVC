    public void listaCliente(){
        List<Categoria>data=model.listaCategoria();
        DefaultTableModel dtm=(DefaultTableModel)table.getModel();
        dtm.setRowCount(0);
        for (Categoria c:data) {
            Object[]fila={ c.getIdCategoria(),
                           c.getNombreRepuesto(),
                           c.getDescripcion()};
            dtm.addRow(fila);
        }
        dtm.fireTableDataChanged();
    }

